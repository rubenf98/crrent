     * @return \Illuminate\Http\Response
     */
    public function update(PriceRequest $request, Price $price)
    {
        $validator = $request->validated();

        $price->update([
            'min' => $validator['min'],
            'max' => $validator['max'],
            'price' => $validator['price'],
        ]);

        return new PriceResource($price);
    }

